package com.jiangdg.poidemos.bean;

/** 书签信息实体
 *
 * Created by jiangdongguo on 2017/10/26.
 */

public class WordBMarkBean {
    private int markNum;
    private String markName;
    private int markStart;
    private int markEnd;

    public int getMarkNum() {
        return markNum;
    }

    public void setMarkNum(int markNum) {
        this.markNum = markNum;
    }

    public String getMarkName() {
        return markName;
    }

    public void setMarkName(String markName) {
        this.markName = markName;
    }

    public int getMarkStart() {
        return markStart;
    }

    public void setMarkStart(int markStart) {
        this.markStart = markStart;
    }

    public int getMarkEnd() {
        return markEnd;
    }

    public void setMarkEnd(int markEnd) {
        this.markEnd = markEnd;
    }
}
